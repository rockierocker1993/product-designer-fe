export function renderLucideSvgIcon(svgString, { size = 24, color = "black" } = {}) {
  return svgString
    .replace(/stroke="currentColor"/g, `stroke="${color}"`)
    .replace(/width=".*?"/, `width="${size}"`)
    .replace(/height=".*?"/, `height="${size}"`);
}

